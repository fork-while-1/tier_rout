"""
Fine-tuning the library models for causal language modeling (GPT, GPT-2, CTRL, ...) on a text file or a dataset.

Here is the full list of checkpoints on the hub that can be fine-tuned by this script:
https://huggingface.co/models?filter=text-generation
"""
# You can also adapt this script on your own causal language modeling task. Pointers for this are left as comments.

import logging
import math
import os
import sys
from typing import Optional
from model_args import ModelArguments
from data_args import DataTrainingArguments

import datasets
import evaluate
import torch

from process_dset import load_processed_dataset
from train_det_router import DecoderModel, DecoderConfig

import transformers
from transformers import (
    CONFIG_MAPPING,
    AutoConfig,
    TrainingArguments,
    AutoModelForCausalLM,
    AutoTokenizer,
    HfArgumentParser,
    Trainer,
    DataCollatorForLanguageModeling,
    is_torch_xla_available,
    set_seed,
)
from transformers.testing_utils import CaptureLogger
from transformers.trainer_utils import get_last_checkpoint

from modeling_monet import MonetForCausalLM, MonetConfig

# from transformers.utils import check_min_version, send_example_telemetry
# from transformers.utils.versions import require_version


# Will error if the minimal version of Transformers is not installed. Remove at your own risks.
